            item { Spacer(modifier = Modifier.height(16.dp)) }
            if (tasks.size < 1) {
                item {
                    TextButton(onClick = { tasks.clear(); tasks.addAll(eco) }) {
                        Text(stringResource(R.string.add_tasks))
                    }
